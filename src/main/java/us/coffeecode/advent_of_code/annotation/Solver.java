package us.coffeecode.advent_of_code.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks a Java method as being a solution for a specific part of an Advent of Code solution. In order to be useful,
 * this annotation should be present on a method contained in a class annotated with {@link AdventOfCodeSolution}.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface Solver {

  int part();

  boolean visual() default false;
}
